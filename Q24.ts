//Test for equality and inequality with strings:
let fruit = "mango"
console.log(fruit === "mango");
console.log(fruit === "Mango");

//Test using lower case function:
let flower = "JASMINE"
console.log(flower.toLocaleLowerCase());

//Test for equality and inequality with numbers:
console.log(24 > 24);
console.log(12 < 16);

//Test using "and" , "or" operator:
console.log(true && false);
console.log(true || false);

//Test for an item is not in array:
let fruits = ["Apple", "Mango", "Orange"]
console.log(`Is "Cherries" in Fruits?`);
console.log(fruits.includes("Cherries"));

